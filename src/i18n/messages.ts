// Flatten the nested EN structure into the same dot-key format used by FA, so
// both locales share a single key namespace.
function flatten(obj: Record<string, unknown>, prefix = ""): Record<string, string> {
  const out: Record<string, string> = {};
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? `${prefix}.${k}` : k;
    if (v && typeof v === "object" && !Array.isArray(v)) {
      Object.assign(out, flatten(v as Record<string, unknown>, key));
    } else {
      out[key] = String(v);
    }
  }
  return out;
}

export const EN_FLAT: Record<string, string> = flatten(
  EN as unknown as Record<string, unknown>
);

export const MESSAGES: Record<Locale, Record<string, string>> = {
  en: EN_FLAT,
  fa: FA,
};

export function isLocale(value: unknown): value is Locale {
  return value === "en" || value === "fa";
}

export function interpolate(
  template: string,
  vars: Record<string, string | number> = {}
): string {
  return template.replace(/\{(\w+)\}/g, (_, k) =>
    Object.prototype.hasOwnProperty.call(vars, k) ? String(vars[k]) : `{${k}}`
  );
}

export function translate(
  locale: Locale,
  key: string,
  vars?: Record<string, string | number>
): string {
  const dict = MESSAGES[locale] ?? MESSAGES.en;
  const fallback = MESSAGES.en;
  const tmpl = dict[key] ?? fallback[key] ?? key;
  return vars ? interpolate(tmpl, vars) : tmpl;
}
